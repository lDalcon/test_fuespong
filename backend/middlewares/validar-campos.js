//===============================================================================
// Imports
//===============================================================================
const { validationResult } = require('express-validator');

//===============================================================================
// Funtions
//===============================================================================
const validarCampos = (req, res, next) => {
    const errores = validationResult(req);
    if (!errores.isEmpty()) {
        return res.status(400).json({
            ok: false,
            message: errores.mapped()
        });
    }
    next();
}

//===============================================================================
// Exports
//===============================================================================

module.exports = {
    validarCampos
}